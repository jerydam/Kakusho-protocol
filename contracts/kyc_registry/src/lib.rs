/// How far current_timestamp (a public input the PROVER supplies) is
/// allowed to drift from the ledger's actual timestamp. The circuit has
/// no way to know real-world time; it only proves internal consistency
/// of (current_timestamp - dob_timestamp) etc. Without this check, a
/// prover could pick any current_timestamp that makes their predicates
/// pass — e.g. a stale or fabricated "current_timestamp" that makes an
/// expired document look fresh. 1 hour gives slack for proving time
/// (browser-side Groth16 proving can take tens of seconds to a few
/// minutes on lower-end devices) plus clock skew, without leaving so
/// wide a window that staleness becomes meaningful.
const MAX_TIMESTAMP_DRIFT_SECONDS: u64 = 3600;

#[contract]
pub struct KycRegistry;

#[contractimpl]
impl KycRegistry {
    pub fn register_integrator(
        env: Env,
        integrator_id: BytesN<32>,
        owner: Address,
        min_age_seconds: u64,
        restricted_root: BytesN<32>,
        doc_max_age_seconds: u64,
    ) {
        integrator::register_integrator(
            &env,
            integrator_id,
            owner,
            min_age_seconds,
            restricted_root,
            doc_max_age_seconds,
        );
    }

    pub fn update_integrator_rules(
        env: Env,
        integrator_id: BytesN<32>,
        min_age_seconds: u64,
        restricted_root: BytesN<32>,
        doc_max_age_seconds: u64,
    ) {
        integrator::update_integrator_rules(
            &env,
            integrator_id,
            min_age_seconds,
            restricted_root,
            doc_max_age_seconds,
        );
    }


    pub fn set_active(env: Env, integrator_id: BytesN<32>, active: bool) {
        integrator::set_active(&env, integrator_id, active);
    }

    pub fn get_integrator(env: Env, integrator_id: BytesN<32>) -> IntegratorConfig {
        integrator::get_integrator(&env, integrator_id)
    }

    pub fn is_nullifier_used(env: Env, integrator_id: BytesN<32>, nullifier: BytesN<32>) -> bool {
        env.storage()
            .persistent()
            .has(&DataKey::Nullifier(integrator_id, nullifier))
    }

    /// Verifies a proof for a specific integrator's rules.
    ///
    /// `nullifier` and `current_timestamp` are passed as explicit,
    /// typed arguments (BytesN<32> and u64) rather than buried inside
    /// `public_signals: Vec<Bn254Fr>`, even though both are also part
    /// of the circuit's public signal vector. This is deliberate: the
    /// SDK must independently re-derive the exact Bn254Fr field
    /// elements the circuit committed to from these plain values and
    /// place them at the correct index of `public_signals` before
    /// calling this function — see sdk/src/prover/snarkjs_worker.ts.
    /// That redundancy is what lets this contract compare against
    /// plain u64/BytesN<32> (cheap, unambiguous) while still passing
    /// the field-element vector groth16_verifier's pairing check
    /// actually needs. If the two disagree, the pairing check fails,
    /// which is the correct outcome — it means the caller tampered
    /// with one without the other.
    ///
    /// public_signals must be in kyc_ocr.circom's declared order:
    /// [current_timestamp, min_age_seconds, restricted_root,
    /// doc_max_age_seconds, integrator_id] — 5 elements; the circuit's
    /// `nullifier` OUTPUT signal is signal [0] of the full public
    /// vector groth16 verifies against, but Soroban's pairing check
    /// here only needs the IC-matched portion; consult
    /// circuits/docs/circuit-spec.md (to be written) for the
    /// authoritative signal ordering before wiring up a real SDK call,
    /// and keep that doc and this comment in sync — a silent mismatch
    /// here is the single easiest way to make this whole protocol
    /// accept invalid proofs.
    ///
    /// On success, records the nullifier so this document can't be
    /// reused for THIS integrator again, and returns true. Does not
    /// mint anything or store PII — callers (the integrating dApp) own
    /// the decision of what "verified" unlocks on their end.
    pub fn verify(
        env: Env,
        integrator_id: BytesN<32>,
        nullifier: BytesN<32>,
        current_timestamp: u64,
        proof_a: BytesN<64>,
        proof_b: BytesN<128>,
        proof_c: BytesN<64>,
        public_signals: Vec<Bn254Fr>,
    ) -> Result<bool, RegistryError> {
        let config: IntegratorConfig = env
            .storage()
            .persistent()
            .get(&DataKey::Integrator(integrator_id.clone()))
            .ok_or(RegistryError::IntegratorNotFound)?;

        if !config.active {
            return Err(RegistryError::IntegratorInactive);
        }

        if public_signals.len() != 6 {
    return Err(RegistryError::WrongPublicInputCount);
}

// ── Timestamp sanity check ── (unchanged)
let ledger_now = env.ledger().timestamp();
let drift = if current_timestamp > ledger_now {
    current_timestamp - ledger_now
} else {
    ledger_now - current_timestamp
};
if drift > MAX_TIMESTAMP_DRIFT_SECONDS {
    return Err(RegistryError::TimestampOutOfRange);
}

// ── Replay protection, namespaced per integrator ── (unchanged)
let nullifier_key = DataKey::Nullifier(integrator_id.clone(), nullifier.clone());
if env.storage().persistent().has(&nullifier_key) {
    return Err(RegistryError::NullifierAlreadyUsed);
}

// ── Bind every public signal to what this proof actually committed to ──
// public_signals order (circuit's authoritative declaration):
// [0] nullifier, [1] current_timestamp, [2] min_age_seconds,
// [3] restricted_root, [4] doc_max_age_seconds, [5] integrator_id
fn u64_to_fr_bytes(env: &Env, val: u64) -> BytesN<32> {
    let mut buf = [0u8; 32];
    buf[24..32].copy_from_slice(&val.to_be_bytes());
    BytesN::from_array(env, &buf)
}

let claimed_nullifier = public_signals.get(0).unwrap();
let claimed_current_timestamp = public_signals.get(1).unwrap();
let claimed_min_age = public_signals.get(2).unwrap();
let claimed_restricted_root = public_signals.get(3).unwrap();
let claimed_max_doc_age = public_signals.get(4).unwrap();
let claimed_integrator_id = public_signals.get(5).unwrap();

let expected_nullifier_bytes = nullifier.clone(); // the BytesN<32> arg, must match the proof's actual output
let expected_current_timestamp_bytes = u64_to_fr_bytes(&env, current_timestamp);
let expected_min_age_bytes = u64_to_fr_bytes(&env, config.min_age_seconds);
let expected_max_doc_age_bytes = u64_to_fr_bytes(&env, config.doc_max_age_seconds);
let expected_root_bytes = config.restricted_root.clone();
let expected_integrator_id_bytes = integrator_id.clone();

if claimed_nullifier.to_bytes() != expected_nullifier_bytes
    || claimed_current_timestamp.to_bytes() != expected_current_timestamp_bytes
    || claimed_min_age.to_bytes() != expected_min_age_bytes
    || claimed_restricted_root.to_bytes() != expected_root_bytes
    || claimed_max_doc_age.to_bytes() != expected_max_doc_age_bytes
    || claimed_integrator_id.to_bytes() != expected_integrator_id_bytes
{
    return Err(RegistryError::PublicInputMismatch);
}

        // ── Groth16 pairing check ──
        let vk = default_vk::load(&env);

        // NOTE: using .to_array() + from_array(env, &arr) rather than a
        // direct from_bytes(BytesN) call — from_array(env, &[u8; N]) is
        // the constructor your original verifier.rs proved works; a
        // bare from_bytes(BytesN<N>) (no env arg) may or may not exist
        // on this type and I won't guess at unverified API surface here.
        let proof = Groth16Proof {
            a: soroban_sdk::crypto::bn254::Bn254G1Affine::from_array(&env, &proof_a.to_array()),
            b: soroban_sdk::crypto::bn254::Bn254G2Affine::from_array(&env, &proof_b.to_array()),
            c: soroban_sdk::crypto::bn254::Bn254G1Affine::from_array(&env, &proof_c.to_array()),
        };

        let valid = Groth16Verifier::verify_proof(env.clone(), vk, proof, public_signals)
            .map_err(|_| RegistryError::InvalidProof)?;

        if !valid {
            return Ok(false);
        }

        env.storage().persistent().set(&nullifier_key, &true);
        Ok(true)
    }
}

