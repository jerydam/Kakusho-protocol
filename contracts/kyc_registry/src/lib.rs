#![no_std]

// kyc_registry — the B2B2C hub contract.
//
// Flow:
//   1. Integrator (a dApp) calls register_integrator() ONCE with their
//      rules (min age, restricted country root, doc freshness window).
//   2. Their end users generate proofs in-browser via the SDK, using
//      THOSE specific public inputs (the SDK fetches the integrator's
//      config via get_integrator() before proving, so the user's proof
//      is built against the right rules from the start).
//   3. The dApp's frontend or backend calls verify(), passing the
//      integrator_id, the proof, and the public signals the user's
//      browser produced.
//   4. This contract checks: (a) the public signals actually match
//      what THIS integrator requires — not just "some valid proof for
//      some integrator" — (b) the nullifier hasn't been used before for
//      this integrator, (c) current_timestamp is sane, then (d) calls
//      groth16_verifier with either DEFAULT_VK or the integrator's
//      custom_vk.
//
// The check in step 4(a) is the part most worth getting right and the
// part most likely to be glossed over: a Groth16 proof being
// cryptographically valid only proves the prover knew SOME witness
// satisfying SOME public inputs. It does NOT by itself prove those
// public inputs are the ones a given integrator cares about. Without
// step 4(a), a user could generate one proof with min_age_seconds=0
// against some other (or no) integrator's rules and replay it
// everywhere, claiming "verified" status under rules nobody actually
// configured. This contract closes that gap by re-deriving "what should
// the public signals be for this integrator" from storage and comparing.

